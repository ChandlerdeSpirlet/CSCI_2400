/* 
 * bitAnd - x&y using only ~ and | 
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
int bitAnd(int x, int y) {//done
    //take inverse of x, y, then take inverse of result ~x or ~y
    int z;
    z = ~(~x | ~y);
    return z;
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {//done
  //Shift x by the n number of bits and take the and
  return (x >> (n << 3)) & 0xff;
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
    //Shift x by n bits
    //Shift the MSB n bits, left shift, then complement
    return (x >> n) & ~(((0x1 << 31) >> n) << 1);
}
/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {//done
  //Create a mask, then check for a 1 at each position
  //Add the number of ones, then move each bit to the edge
  int mask_1;
  int a, b, c, d, e, f, g, h;
  int temp;
  int A, B, C, D;
  int mask_2;
  int result;

  mask_1 = 1 | (1 << 8);
  mask_1 = mask_1 | (mask_1 << 16);  //1010101
    // 1's at end of every byte: [00000001]

  a = (x) & mask_1;
  b = (x >> 1) & mask_1; 
    //Checks for a 1 in this position
  c = (x >> 2) & mask_1;
  d = (x >> 3) & mask_1;
  e = (x >> 4) & mask_1;
  f = (x >> 5) & mask_1;
  g = (x >> 6) & mask_1;
  h = (x >> 7) & mask_1;
  temp = a + b + c + d + e + f + g + h; 
    //Add number of 1's
  mask_2 = ~((~0) << 8);  //0xff = 11111111    
    // only last byte is 1: 00...0011111111
  A = mask_2 & temp;
  B = mask_2 & (temp >> 8); 
    //Shifting bytes to edge
  C = mask_2 & (temp >> 16);
  D = mask_2 & (temp >> 24);
  result = (A + B + C + D); 
    //Add ones
  return result;
}
